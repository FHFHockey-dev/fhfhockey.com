export function getPredictionSourceOwnership(table: string): PredictionSourceOwnership | null {
  return PREDICTION_SOURCE_OWNERSHIP.find((entry) => entry.table === table) ?? null;
}

export function getPredictionSourceOwnershipByStage(
  stage: PredictionPipelineStage,
): PredictionSourceOwnership[] {
  return PREDICTION_SOURCE_OWNERSHIP.filter((entry) => entry.stage === stage);
}
