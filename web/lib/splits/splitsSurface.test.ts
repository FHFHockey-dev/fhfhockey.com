describe("buildPlayerVsTeamSummary", () => {
  it("packages a player baseline and opponent split into one comparison payload", () => {
    const summary = buildPlayerVsTeamSummary({
      baselineRow: {
        playerId: 97,
        playerName: "Star Skater",
        teamLabel: "EDM",
        gamesPlayed: 68,
        toiPerGameSeconds: 1210,
        shotsPer60: 10.2,
        totalPointsPer60: 4.0,
        ixgPer60: 1.3,
        goalsPer60: 1.4,
      },
      versusOpponentRow: {
        playerId: 97,
        playerName: "Star Skater",
        teamLabel: "EDM",
        gamesPlayed: 4,
        toiPerGameSeconds: 1225,
        shotsPer60: 11.1,
        totalPointsPer60: 4.8,
        ixgPer60: 1.5,
        goalsPer60: 1.8,
      },
      opponentLabel: "VAN",
      ppShotSharePct: 0.41,
    });

    expect(summary).toMatchObject({
      playerId: 97,
      playerName: "Star Skater",
      opponentLabel: "VAN",
      season: {
        gamesPlayed: 68,
        ppShotSharePct: 0.41,
      },
      versusOpponent: {
        gamesPlayed: 4,
        shotsPer60: 11.1,
      },
    });
  });
});
