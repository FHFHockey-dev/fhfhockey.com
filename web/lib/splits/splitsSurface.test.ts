describe("buildPpShotShareRows", () => {
  it("derives team-level PP shot share from player shot totals", () => {
    const rows = buildPpShotShareRows([
      {
        playerId: 1,
        playerName: "Shooter A",
        positionCode: "RW",
        shots: 12,
      },
      {
        playerId: 2,
        playerName: "Shooter B",
        positionCode: "D",
        shots: 8,
      },
    ]);

    expect(rows).toHaveLength(2);
    expect(rows[0]).toMatchObject({
      playerId: 1,
      ppShots: 12,
    });
    expect(rows[0].ppShotSharePct).toBeCloseTo(0.6);
    expect(rows[1].ppShotSharePct).toBeCloseTo(0.4);
  });
});

describe("buildTeamLeaderRows", () => {
  it("adds PP shot share into the fantasy pulse ranking", () => {
    const leaders = buildTeamLeaderRows({
      leaderRows: [
        {
          playerId: 10,
          playerName: "Driver One",
          positionCode: "C",
          teamLabel: "EDM",
          shotsPer60: 11,
          totalPointsPer60: 3,
          ixgPer60: 1.4,
        },
        {
          playerId: 11,
          playerName: "Driver Two",
          positionCode: "RW",
          teamLabel: "EDM",
          shotsPer60: 10,
          totalPointsPer60: 2.8,
          ixgPer60: 1.3,
        },
      ],
      ppShotShareRows: [
        {
          playerId: 11,
          playerName: "Driver Two",
          positionCode: "RW",
          ppShots: 14,
          ppShotSharePct: 0.7,
        },
        {
          playerId: 10,
          playerName: "Driver One",
          positionCode: "C",
          ppShots: 6,
          ppShotSharePct: 0.3,
        },
      ],
    });

    expect(leaders[0]?.playerId).toBe(11);
    expect(leaders[0]?.ppShotSharePct).toBeCloseTo(0.7);
    expect(leaders[0]?.fantasyPulse).toBeGreaterThan(
      leaders[1]?.fantasyPulse ?? 0
    );
  });
});

